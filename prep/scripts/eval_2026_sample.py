"""Evaluate every predictor we have against the 2026 contamination-free
sample (post-Grok-training-cutoff).

Loads `llm_calls_xN_seed42.jsonl` (generated by sample_llm_test_points.py)
and scores every method with Brier + ECE. Includes:

  - market alone (the floor to beat)
  - fair_price (with REAL n_event from sibling lookups — paper says
    this is where the alpha lives)
  - any grok jsonl path passed via --grok-preds (so we can re-use
    saved predictions even when the API key is dead)
  - logit-pool ensembles of the above

Bootstrap confidence intervals at N=190 so we can see whether deltas
exceed the team's documented σ ≈ 0.011 noise floor.

Usage:
    python prep/scripts/eval_2026_sample.py \\
        --sample prep_handoff/Kalshitopvolmarkets/samples/llm_calls_x10_seed42.jsonl \\
        --weekly-dir prep_handoff/Kalshitopvolmarkets/markets \\
        --grok-preds prep/data/predictions/grok_2026_x10.jsonl   # optional
"""

from __future__ import annotations

import argparse
import json
import math
import random
import sys
from collections import Counter, defaultdict
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1] / "src"))

from prep.baselines.fair_price_v0 import fair_price_one  # noqa: E402
from prep.aggregator import logit_pool  # noqa: E402
from prep.score import brier, ece  # noqa: E402


def load_sample(path: Path) -> list[dict]:
    return [json.loads(l) for l in path.read_text().splitlines() if l.strip()]


def load_event_size_lookup(weekly_dir: Path) -> dict[str, int]:
    """Build event_ticker → number-of-markets-in-event from the weekly market lists.

    This mirrors what the live trading agent would see — the candidate
    set is the universe of markets per tick. Per docs, n_event from the
    candidate set is the difference between Mode A (Brier 0.181) and
    Mode C (Brier 0.171).
    """
    by_event: defaultdict[str, set[str]] = defaultdict(set)
    for f in sorted(weekly_dir.glob("*_selected_markets.jsonl")):
        for line in f.read_text().splitlines():
            if not line.strip():
                continue
            row = json.loads(line)
            et = row.get("event_ticker")
            tk = row.get("ticker")
            if et and tk:
                by_event[et].add(tk)
    return {et: len(ts) for et, ts in by_event.items()}


def bootstrap_ci(values: list[float], outcomes: list[int], n_boot: int = 1500, seed: int = 42) -> tuple[float, float, float]:
    """Brier mean + 95% CI half-width via resample bootstrap."""
    n = len(values)
    rng = random.Random(seed)
    briers = []
    for _ in range(n_boot):
        idx = [rng.randrange(n) for _ in range(n)]
        ps = [values[i] for i in idx]
        os_ = [outcomes[i] for i in idx]
        briers.append(sum((p - o) ** 2 for p, o in zip(ps, os_)) / n)
    briers.sort()
    mean = sum(briers) / len(briers)
    lo = briers[int(0.025 * n_boot)]
    hi = briers[int(0.975 * n_boot)]
    return mean, lo, hi


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--sample", required=True, type=Path)
    parser.add_argument(
        "--weekly-dir",
        type=Path,
        default=Path("prep_handoff/Kalshitopvolmarkets/markets"),
        help="Directory of *_selected_markets.jsonl files (for n_event lookup).",
    )
    parser.add_argument(
        "--grok-preds",
        type=Path,
        default=None,
        help="Optional jsonl with {market_ticker, p_yes} from a prior Grok run.",
    )
    parser.add_argument("--bootstrap", type=int, default=1500)
    args = parser.parse_args()

    samples = load_sample(args.sample)
    raw_n = len(samples)
    samples = [s for s in samples if s.get("outcome_yes") in (0, 1)]
    print(f"Loaded {raw_n} samples from {args.sample} ({len(samples)} resolved with outcome)", flush=True)
    print(f"  categories: {Counter(s['event']['category'] for s in samples).most_common()}")

    n_event_lookup = load_event_size_lookup(args.weekly_dir)
    print(f"  built n_event lookup for {len(n_event_lookup)} events from weekly market files")

    # Extract per-sample inputs.
    outcomes: list[int] = []
    market_mids: list[float] = []
    fair_preds: list[float] = []
    tickers: list[str] = []
    for s in samples:
        outcomes.append(int(s["outcome_yes"]))
        q = float(s["quote"]["market_mid"])
        market_mids.append(q)
        et = s["event"].get("event_ticker", "")
        n_event = max(1, n_event_lookup.get(et, 1))
        fair_preds.append(fair_price_one(q, n_event=n_event))
        tickers.append(s["ticker"])

    # Optional Grok predictions.
    grok_preds: list[float] | None = None
    if args.grok_preds and args.grok_preds.exists():
        gd = {}
        for line in args.grok_preds.read_text().splitlines():
            if not line.strip():
                continue
            r = json.loads(line)
            gd[r.get("market_ticker") or r.get("ticker")] = float(r["p_yes"])
        grok_preds = [gd.get(t) for t in tickers]
        coverage = sum(1 for p in grok_preds if p is not None)
        print(f"  grok coverage: {coverage}/{len(samples)} from {args.grok_preds}")
        if coverage < len(samples):
            # Fill missing with market price (neutral fallback for ensembles)
            grok_preds = [p if p is not None else m for p, m in zip(grok_preds, market_mids)]

    # Build methods to evaluate.
    methods: dict[str, list[float]] = {
        "market alone": market_mids,
        "fair_price (real n_event)": fair_preds,
    }
    if grok_preds:
        methods["grok v3 (saved)"] = grok_preds
        # Ensembles
        methods["logit-pool: market + fair_price"] = [logit_pool([m, f]) for m, f in zip(market_mids, fair_preds)]
        methods["logit-pool: grok + market"] = [logit_pool([g, m]) for g, m in zip(grok_preds, market_mids)]
        methods["logit-pool: grok + fair_price"] = [logit_pool([g, f]) for g, f in zip(grok_preds, fair_preds)]
        methods["logit-pool: grok + market + fair"] = [logit_pool([g, m, f]) for g, m, f in zip(grok_preds, market_mids, fair_preds)]
        # Conservative ensembles (heavy shrinkage to data anchor)
        methods["0.3·grok + 0.7·fair_price (logit)"] = [logit_pool([g, f], [0.3, 0.7]) for g, f in zip(grok_preds, fair_preds)]
    else:
        methods["logit-pool: market + fair_price"] = [logit_pool([m, f]) for m, f in zip(market_mids, fair_preds)]

    # Score table.
    print()
    print(f"{'method':<40}{'N':>5}{'Brier':>9}{'95% CI':>22}{'ECE':>8}")
    print("-" * 84)
    market_brier_mean = None
    for label, preds in methods.items():
        b = brier(preds, outcomes)
        e = ece(preds, outcomes)
        m, lo, hi = bootstrap_ci(preds, outcomes, n_boot=args.bootstrap)
        if label == "market alone":
            market_brier_mean = m
        ci = f"[{lo:.4f}, {hi:.4f}]"
        rel = ""
        if market_brier_mean is not None and label != "market alone":
            delta = (m - market_brier_mean) * 100  # percentage points
            rel = f"  {delta:+.2f}pp vs mkt"
        print(f"{label:<40}{len(preds):>5}{b:>9.4f}{ci:>22}{e:>8.4f}{rel}")

    print()
    print(f"Class balance: YES={sum(outcomes)}, NO={len(outcomes)-sum(outcomes)}")
    print(f"N=190 noise floor (paper σ): ~0.011 Brier, 95% CI half-width ~0.021")
    print(f"=> Any method within ±0.02 Brier of market is statistically indistinguishable.")

    # Per-category breakdown.
    cats = [s["event"]["category"] for s in samples]
    by_cat: dict[str, list[int]] = defaultdict(list)
    for i, c in enumerate(cats):
        by_cat[c].append(i)
    if len(by_cat) > 1:
        print()
        print("Per-category Brier:")
        header = f"  {'category':<14}{'N':>5}"
        for label in methods:
            header += f"  {label[:18]:>20}"
        print(header)
        for cat in sorted(by_cat, key=lambda c: -len(by_cat[c])):
            idxs = by_cat[cat]
            if len(idxs) < 5:
                continue
            line = f"  {cat:<14}{len(idxs):>5}"
            cat_outcomes = [outcomes[i] for i in idxs]
            for label, preds in methods.items():
                cat_preds = [preds[i] for i in idxs]
                line += f"  {brier(cat_preds, cat_outcomes):>20.4f}"
            print(line)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
